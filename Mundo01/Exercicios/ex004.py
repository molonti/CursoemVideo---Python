a = input('Digite algo: ')
print('O tipo primitivo desse valor é', type(a))
print('Tem espaços?', a.isspace())
print('Número: ', a.isnumeric())
print('Alfabétioo: ', a.isalpha())
print('Alfanumérico', a.isalnum())
print('Maiúsculas', a.isupper())
print('Minúsculas', a.islower())
print('Capitalizado', a.istitle())





